<template>
  <div class="addTodo">
      <form @submit.prevent="onSubmit(title)">
          <input type="text" v-model="title" placeholder="Add a ToDo">
          <input type="submit" value="Submit">
      </form>
  </div>
</template>

<script>
import { mapActions } from 'vuex'
export default {
    name: "AddToDo",
    data() {
        return {
            title: ''
        };
    },
    methods: {
        ...mapActions(['addTodo']), 
        onSubmit(title) {
            this.addTodo(title);
            this.title =''
        }
    }
}
</script>

